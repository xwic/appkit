	/**
	 * @param tag
	 * @return the flag image for the given color
	 */
	public static ImageRef getFlagForColor(String color) {
		if ("yellow".equals(color)) {
			return IMAGE_FLAG_YELLOW;
		} else if ("red".equals(color)) {
			return IMAGE_FLAG_RED;
		} else if ("blue".equals(color)) {
			return IMAGE_FLAG_BLUE;
		} else if ("orange".equals(color)) {
			return IMAGE_FLAG_ORANGE;
		} else if ("green".equals(color)) {
			return IMAGE_FLAG_GREEN;
		} else if ("pink".equals(color)) {
			return IMAGE_FLAG_PINK;
		} else if ("purple".equals(color)) {
			return IMAGE_FLAG_PURPLE;
		}

		return null;
	}

